        // JSON-Driven Image Configuration System (matching team2.html)
        async function applyTeamImageConfig() {
            try {

                const config = TeamImageConfig;              
                
                
                // Apply settings to each team member image
                Object.entries(config.teamImageSettings).forEach(([id, settings]) => {
                    const img = document.querySelector(`img[data-member="${settings.name}"]`);
                    if (img) {
                        // Apply CSS custom properties for dynamic styling
                        img.style.setProperty('--img-scale-x', `${settings.scaleX}%`);
                        img.style.setProperty('--img-scale-y', settings.scaleY / 100);
                        img.style.setProperty('--img-offset-y', `${settings.offsetY}px`);
                        img.style.setProperty('--img-position', settings.objectPosition);
                        
                        // Mobile-specific scaling (slightly wider for better proportions)
                        const mobileScaleX = Math.min(settings.scaleX + 7, 97); // +7% on mobile, max 97%
                        img.style.setProperty('--img-mobile-scale-x', `${mobileScaleX}%`);
                        
                        // Tablet-specific scaling
                        const tabletScaleX = Math.min(settings.scaleX + 3, 93); // +3% on tablet, max 93%
                        img.style.setProperty('--img-tablet-scale-x', `${tabletScaleX}%`);
                        
                        // Add debug info
                        img.setAttribute('title', `${settings.name} - ${settings.note}`);                        
                        
                    } else {
                        console.warn(`Image element not found for member: ${settings.name}`);
                    }
                });
                
                console.log('Team image configuration applied successfully!');
                
            } catch (error) {
                console.error('Error loading team image configuration:', error);
                console.log('Using default image settings');
                
                // Fallback to default settings with responsive scaling
                document.querySelectorAll('.team-card img').forEach(img => {
                    img.style.setProperty('--img-scale-x', '85%');
                    img.style.setProperty('--img-scale-y', '0.95');
                    img.style.setProperty('--img-offset-y', '-3px');
                    img.style.setProperty('--img-position', 'center center');
                    img.style.setProperty('--img-mobile-scale-x', '92%');
                    img.style.setProperty('--img-tablet-scale-x', '88%');
                });
            }
        }

